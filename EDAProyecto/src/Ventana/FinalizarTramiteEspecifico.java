    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:}
        
        try{
        String input= jTextField1.getText().trim();
        
        
        if (input.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Debe ingresar un ID de tr치mite.","",JOptionPane.WARNING_MESSAGE);
            return;
        }
        
        int id = Integer.parseInt(this.jTextField1.getText());
        control.FinalizarTramiteEspecifico(id);
        
        LocalTime horaActual = LocalTime.now();
        DateTimeFormatter formato = DateTimeFormatter.ofPattern("HH:mm");
        String hora = horaActual.format(formato);
        JOptionPane.showMessageDialog(null, "El tr치mite se ha finalizado el: " + hora);
        jTextField1.setText(null);
        }catch(NullPointerException e){
            JOptionPane.showMessageDialog(null, "No se encontr칩 el tr치mite!","",JOptionPane.ERROR_MESSAGE);
        }       
    }//GEN-LAST:event_jButton1ActionPerformed

